package io.itjun.basic.chap02;

public class MathCalc {

    public static void main(String[] args) {
        System.out.println("5 + 6");
        System.out.println(5 + 6);
        System.out.println(5 - 6);
        System.out.println(5 * 6);
        System.out.println(5 / 6.0d);
        System.out.println((1 + 2 + 3) * 4 / 5.0);
    }

}
